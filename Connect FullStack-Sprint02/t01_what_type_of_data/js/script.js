// Number
let myNumber = 42;

// BigInt
let myBigInt = 1234567890123456789012345678901234567890n;

// String
let myString = "Hello, world!";

// Boolean
let myBoolean = true;

// Null
let myNull = null;

// Undefined
let myUndefined = undefined;

// Object
let myObject = { name: "John", age: 25 };

// Symbol
let mySymbol = Symbol("unique");

// Function
let myFunction = function() {
};

// Display variable names and data types using alert()
alert(`myNumber is ${typeof myNumber}
myBigInt is ${typeof myBigInt}
myString is ${typeof myString}
myBoolean is ${typeof myBoolean}
myNull is ${typeof myNull}
myUndefined is ${typeof myUndefined}
myObject is ${typeof myObject}
mySymbol is ${typeof mySymbol}
myFunction is ${typeof myFunction}`);
